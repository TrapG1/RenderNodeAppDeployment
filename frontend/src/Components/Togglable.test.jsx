import { render, screen } from '@testing-library/react'
import userEvent from '@testing-library/user-event'
import Togglable from './Togglable'

describe('<Togglable />', () => {
  let container

  beforeEach(() => {
    container = render(
      <Togglable buttonLabel="show..." quitButtonLabel="hide">
        <div className="testDiv" >
          togglable content
        </div>
      </Togglable>
    ).container
  })

  test('renders its children', async () => {
    await screen.findAllByText('show...')
  })

  test('at start the children are not displayed', () => {
    expect(screen.queryByText('togglable content')).not.toBeInTheDocument()
  })


  test('after clicking the button, children are displayed', async () => {
    const user = userEvent.setup()
    const button = screen.getByText('show...')
    await user.click(button)

    expect(screen.queryByText('togglable content')).toBeInTheDocument()
  })

})